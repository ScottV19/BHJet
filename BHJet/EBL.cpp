// Now define the functions that do the EBL attenuation correction. One function for each model.
// Function to correct luminosity for EBL attenuation, model by Dominguez et al. (2011)
void ebl_atten_dom(int size,double* en,double* lum,double redsh){
    gsl_interp_accel *acc_TeV =  gsl_interp_accel_alloc();
    gsl_interp_accel *acc_redshift =  gsl_interp_accel_alloc();
    gsl_spline2d *tau_dom = gsl_spline2d_alloc(gsl_interp2d_bicubic,39,50);
    gsl_spline2d_init(tau_dom,dom_redshift_table,dom_TeV_table,dom_ebl_table,39,50);
    
    if(redsh > dom_redshift_table[0] && redsh < dom_redshift_table[38]){
    	for (int k=0;k<size;k++){
    		if (lum[k] < 10){
    			break;
    		} else if (en[k]/1.6 > dom_TeV_table[0] && en[k]/1.6 < dom_TeV_table[49]){
    			if (gsl_spline2d_eval(tau_dom,redsh,en[k]/1.6,acc_redshift,acc_TeV) >= 0.){
    				lum[k] = lum[k]*exp(-1.*gsl_spline2d_eval(tau_dom,redsh,en[k]/1.6,acc_redshift,acc_TeV));
    			}
    		}
    	}
    } else if(redsh < dom_redshift_table[0]){
    	std::cout << "Redshift is too small for EBL model(0.01 < redshift < 2.00). No interpolation possible." << std::endl;
    } else{
    	std::cout << "Redshift is too large for EBL model(0.01 < redshift < 2.00). No interpolation possible." << std::endl;
    }
    gsl_interp_accel_free(acc_TeV);
    gsl_interp_accel_free(acc_redshift);
    gsl_spline2d_free(tau_dom);
}

